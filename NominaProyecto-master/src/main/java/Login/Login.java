    private void cargarUsuariosDesdeArchivo() {
        this.users = new ArrayList<>(); 
        try {
            List<String> lineas = manejadorUsuarios.leerTodasLasLineas(); 
            for (String linea : lineas) {
                String[] parts = linea.split(","); 
                if (parts.length >= 3) {
                    try {
                        int accessLevel = Integer.parseInt(parts[0].trim());
                        String username = parts[1].trim();
                        String password = parts[2].trim();
                        this.users.add(new User(username, password, accessLevel));
                    } catch (NumberFormatException e) {
                        System.err.println("Error de formato numérico en la línea: " + linea);
                    }
                }
            }
          //  JOptionPane.showMessageDialog(this, "Se cargaron " + this.users.size() + " usuarios.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error fatal al leer el archivo de usuarios: " + e.getMessage(), "Error de Archivo", JOptionPane.ERROR_MESSAGE); //
        }
    }

