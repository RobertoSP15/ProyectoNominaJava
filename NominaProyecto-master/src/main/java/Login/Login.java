    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String enteredUsername = tfUsuario.getText().trim();
        String enteredPassword = new String(tfPassword.getPassword()).trim();

        if (enteredUsername.isEmpty() || enteredPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese usuario y contraseña.", "Campos Obligatorios", JOptionPane.WARNING_MESSAGE);
            return;
        }

        User authenticatedUser = null;
        for (User user : users) {
            if (user.username.equals(enteredUsername) && user.password.equals(enteredPassword)) {
                authenticatedUser = user;
                break;
            }
        }

   if (authenticatedUser != null) {
    String nombreCompleto = authenticatedUser.username;
    int nivelDeAcceso = authenticatedUser.accessLevel;

    try {
        List<String> lineas = manejadorUsuarios.leerTodasLasLineas();
        for (String linea : lineas) {
            String[] parts = linea.split(",");
            if (parts.length > 4 && parts[1].trim().equals(authenticatedUser.username)) {
                nombreCompleto = parts[3].trim() + " " + parts[4].trim();
                break;
            }
        }
    } catch (Exception e) {
        System.err.println("Error al leer archivo para obtener nombre completo: " + e.getMessage());
    }

    JOptionPane.showMessageDialog(this, "¡Bienvenido, " + nombreCompleto + "!", "Inicio de Sesión Exitoso", JOptionPane.INFORMATION_MESSAGE);

    Menu menuPrincipal = new Menu(nombreCompleto, nivelDeAcceso);
    menuPrincipal.setVisible(true);
    
    this.dispose(); 
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.", "Error de Inicio de Sesión", JOptionPane.ERROR_MESSAGE);
            tfPassword.setText("");
            tfUsuario.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

